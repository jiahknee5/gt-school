// Generate the Supabase seed SQL (app_form + program-isolated stores) from the SAME deterministic
// generator that seeded HubSpot — so emails/ext-ids line up and reconciliation is provable.
// Emits db/03_seed.sql. No DB connection needed.  Run: node scripts/gen-supabase-sql.mjs

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { generate } from './lib/data-model.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const { families, deals } = generate({ count: 300 });

const s = (v) => (v == null || v === '' ? 'null' : `'${String(v).replace(/'/g, "''")}'`);
const num = (v) => (v == null || Number.isNaN(Number(v)) ? 'null' : String(v));
const ts = (ms, on) => (on ? `to_timestamp(${Number(ms)} / 1000.0)` : 'null');
const cents = (a) => Math.round(Number(a) * 100);
const gradeLabel = (b) => ({ k_2: 'K-2', '3_5': '3-5', '6_8': '6-8', '9_12': '9-12' }[b] ?? null);
// app_form is source of truth → use the TRUE income (HubSpot was seeded with the wrong value on conflicts)
const trueIncome = (f) => {
  const c = f.note.find((x) => x.startsWith('income_conflict:true='));
  return c ? c.split('=')[1] : f.income_band;
};
// the duplicate contact exists ONLY in HubSpot — leaving it out of app_form lets reconciliation flag it
const isDup = (f) => f.extId.includes('dup') || f.note.some((x) => x.startsWith('duplicate_of'));

const out = [];
out.push('-- AUTO-GENERATED by scripts/gen-supabase-sql.mjs — do not hand-edit.');
out.push('-- Source-of-truth funnel data (app_form) + program-isolated stores. Run AFTER db/01_schema.sql.');
out.push('begin;');
out.push('truncate summer.registrations, fall.enrollments, public.app_form restart identity cascade;');

out.push('\ninsert into public.app_form (ext_id, email, parent_first, parent_last, child_first, child_grade, grade_band, income_band, geo, state, persona, tier, engagement_tier, utm_source, follows_alpha_on_x, esa_status, lead_score, program_interest) values');
const af = families.filter((f) => !isDup(f)).map((f) =>
  `  (${s(f.extId)}, ${s(f.email)}, ${s(f.first)}, ${s(f.last)}, ${s(f.child)}, ${s(gradeLabel(f.grade_band))}, ${s(f.grade_band)}, ${s(trueIncome(f))}, ${s(f.geo)}, ${s(f.state)}, ${s(f.persona)}, ${s(f.tier)}, ${s(f.engagement_tier)}, ${s(f.utm_source)}, ${s(f.follows_alpha_on_x)}, ${s(f.esa_status)}, ${num(f.score)}, ${s(f.program_interest)})`
);
out.push(af.join(',\n') + ';');

const fall = deals.filter((d) => d.program === 'fall_anywhere' || d.program === 'flagship');
out.push('\ninsert into fall.enrollments (ext_id, app_form_ext_id, stage, amount_cents, esa_covered, paid_at) values');
out.push(fall.map((d) =>
  `  (${s(d.extId)}, ${s(d.familyExtId)}, ${s(d.stage)}, ${num(cents(d.amount))}, ${s(d.props.gt_esa_covered)}, ${ts(d.closedateMs, d.stage === 'deposit')})`
).join(',\n') + ';');

const summer = deals.filter((d) => d.program === 'summer_camp');
out.push('\ninsert into summer.registrations (ext_id, app_form_ext_id, campus, weeks, stage, amount_cents, paid_at) values');
out.push(summer.map((d) =>
  `  (${s(d.extId)}, ${s(d.familyExtId)}, ${s(d.props.gt_campus)}, ${num(d.props.gt_session_weeks)}, ${s(d.stage)}, ${num(cents(d.amount))}, ${ts(d.closedateMs, d.stage === 'paid' || d.stage === 'attended')})`
).join(',\n') + ';');

out.push('\ncommit;');
out.push("\nselect 'app_form' as t, count(*) from public.app_form union all select 'fall', count(*) from fall.enrollments union all select 'summer', count(*) from summer.registrations;");

const file = join(ROOT, 'db', '03_seed.sql');
writeFileSync(file, out.join('\n') + '\n');
console.log('Wrote', file);
console.log(`app_form: ${af.length} rows · fall.enrollments: ${fall.length} · summer.registrations: ${summer.length}`);
